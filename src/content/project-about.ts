export const PROJECT_ABOUT_MARKDOWN = `## 介绍

**ApiMocktle** —— 一个面向接口设计、文档整理与项目协作的前端项目，当前基于 React Router + Vite + Antd + TypeScript + TailwindCSS 开发。它延续了熟悉的 API 管理台交互，同时保持源码可审计、可本地运行、可按需修改。

名字由来：**Mock**（模拟，鹦鹉学舌）+ **Turtle**（龟）→ Mocktle。突出 API 模拟能力（鹦鹉特性），壳（shell）象征数据结构稳定。

## 致谢

本项目的界面与交互参考了 [Codennnn / Apifox-UI](https://github.com/Codennnn/Apifox-UI)。感谢原作者提供高质量的 UI 设计还原与开源分享，这个项目在此基础上继续做了适配和演进。

## 动机

这个项目的直接起点，是对 Apifox 投毒漏洞问题的关注。相比依赖不可控的客户端环境，我更希望把常用的接口管理能力放到一个可以自行审查、运行和改造的项目里，在保留熟悉使用体验的同时，把主动权拿回到自己手上。`
